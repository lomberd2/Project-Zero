package Statistik;

public class Minimum {
    public static int minimum(int zahl1, int zahl2){
        return Math.min(zahl1,zahl2);
    }
    public static double minimum(double zahl1, double zahl2){
        return Math.min(zahl1, zahl2);
    }
    public static long minimum(long zahl1, long zahl2){
        return Math.min(zahl1, zahl2);
    }
    public static float minimum(float zahl1, float zahl2){
        return Math.min(zahl1, zahl2);
    }
}
