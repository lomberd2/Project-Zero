public class Maximum {
    public static int maximum(int zahl1, int zahl2) {
        return Math.max(zahl1,zahl2);
    }
    public static double maximum(double zahl1, double zahl2){
        return Math.max(zahl1, zahl2);
    }
    public static long maximum(long zahl1, long zahl2){
        return Math.max(zahl1, zahl2);
    }
    public static float maximum(float zahl1, float zahl2){
        return Math.max(zahl1, zahl2);
    }
}